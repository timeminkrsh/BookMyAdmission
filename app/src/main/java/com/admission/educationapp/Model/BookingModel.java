package com.admission.educationapp.Model;

public class BookingModel {
    public String book_id;
    public String book_uniname;
    public String book_clgname;
    public String book_course;
    public String book_quota;
    public String book_status;

    public String getBook_id() {
        return book_id;
    }

    public void setBook_id(String book_id) {
        this.book_id = book_id;
    }

    public String getBook_uniname() {
        return book_uniname;
    }

    public void setBook_uniname(String book_uniname) {
        this.book_uniname = book_uniname;
    }

    public String getBook_clgname() {
        return book_clgname;
    }

    public void setBook_clgname(String book_clgname) {
        this.book_clgname = book_clgname;
    }

    public String getBook_course() {
        return book_course;
    }

    public void setBook_course(String book_course) {
        this.book_course = book_course;
    }

    public String getBook_quota() {
        return book_quota;
    }

    public void setBook_quota(String book_quota) {
        this.book_quota = book_quota;
    }

    public String getBook_status() {
        return book_status;
    }

    public void setBook_status(String book_status) {
        this.book_status = book_status;
    }
}
