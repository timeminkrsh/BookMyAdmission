package com.admission.educationapp.Model;

public class UniversityModel {
    public String uni_name;
    public String uni_id;
    public String uni_image;

    public String getUni_image() {
        return uni_image;
    }

    public void setUni_image(String uni_image) {
        this.uni_image = uni_image;
    }

    public String getUni_name() {
        return uni_name;
    }

    public void setUni_name(String uni_name) {
        this.uni_name = uni_name;
    }

    public String getUni_id() {
        return uni_id;
    }

    public void setUni_id(String uni_id) {
        this.uni_id = uni_id;
    }
}
